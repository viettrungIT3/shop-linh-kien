                        <script>
                            const tableBody = document.querySelector('#datatable tbody');
                            const paginationContainer = document.querySelector('#pagination-container');

                            const ROWS_PER_PAGE = 10;
                            let currentPage = 1;

                            // Tính toán số trang và tạo các nút phân trang
                            const numRows = tableBody.querySelectorAll('tr').length;
                            const numPages = Math.ceil(numRows / ROWS_PER_PAGE);

                            for (let i = 1; i <= numPages; i++) {
                                const button = document.createElement('button');
                                button.classList.add('pagination-button');
                                button.classList.add('page-item');
                                button.textContent = i;
                                button.addEventListener('click', () => {
                                    currentPage = i;
                                    updateTable();
                                });
                                paginationContainer.appendChild(button);
                            }

                            // Cập nhật bảng khi chuyển trang
                            function updateTable() {
                                const start = (currentPage - 1) * ROWS_PER_PAGE;
                                const end = start + ROWS_PER_PAGE;
                                const rows = Array.from(tableBody.querySelectorAll('tr'));

                                rows.forEach((row, index) => {
                                    if (index >= start && index < end) {
                                        row.style.display = '';
                                    } else {
                                        row.style.display = 'none';
                                    }
                                });
                            }

                            // Hiển thị trang đầu tiên khi trang được tải lên
                            updateTable();
                        </script>

